    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // button signup
        String username = textField1.getText();
        String email = textField2.getText();
        String password = textField3.getText();
        boolean isStudent = jCheckBox1.isSelected();
        String role = isStudent ? "student" : "instructor";
        String userId = String.valueOf(generateUserId());
        
            if (username.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Username is required!");
                return;
            }

            if (email.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Email is required!");
                return;
            }

            if (password.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Password is required!");
                return;
            }

        User userFile = new User("src/users.json");
        userFile.load();
       
        SignUp signUp = new SignUp();

        if ( signUp .validateuserId(userId))
        {
            JOptionPane.showMessageDialog(this, "User ID already exist");
            return;
        }
       if (signUp.validateuserEmail(email) == 1) {
        JOptionPane.showMessageDialog(this, "Invalid email format!");
         return;
        
        }
        if (signUp.validateuserEmail(email) == 2) {
            JOptionPane.showMessageDialog(this, "Email already exists!");
             return;
           
        }
            
        else
        {
            password = SignUp.hashPassword(password);
            if (isStudent) {
                
                Student newUser = new Student(userId, role, username, email, password, 0.0);
                signUp.AddStudent(newUser);
                this.setVisible(false);
                new StudentDashboardFrame(newUser).setVisible(true);

            } else {
                Instructor newUser;
                try {
                    newUser = new Instructor(userId, role, username, email, password);
                     signUp.AddInstructor(newUser);
                     
                // new InstructorDashboardFrame(newUser);
                } catch (IOException e) {
                    
                    e.printStackTrace();
                }
               
            }
           userFile.save();
            
           JOptionPane.showMessageDialog(this, "User added successfully!");
 
        }
        
       



    }
      private int generateUserId() {
      
        return (int) (Math.random() * 10000);
    }                                     

